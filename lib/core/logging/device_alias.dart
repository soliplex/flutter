/// Generates a memorable device alias from a UUID.
///
/// Produces a deterministic three-word name like "calm-falcon-ridge"
/// from a UUID. Same ID always yields the same alias.
/// With 64 words per list: 64^3 = 262,144 unique combinations.
String generateDeviceAlias(String id) {
  final hex = id.replaceAll('-', '');
  if (hex.length < 12) return 'unknown-device-alias';
  final a = int.parse(hex.substring(0, 4), radix: 16);
  final b = int.parse(hex.substring(4, 8), radix: 16);
  final c = int.parse(hex.substring(8, 12), radix: 16);

  return '${_adjectives[a % _adjectives.length]}-'
      '${_animals[b % _animals.length]}-'
      '${_places[c % _places.length]}';
}

